public class Rectangle {
    public int LeftX;
    public int Width;
    public int BottomY;
    public int Height;

    public Rectangle()
    {

    }

    public Rectangle(int leftX, int width, int bottomY, int height)
    {
        LeftX = leftX;
        Width = width;
        BottomY = bottomY;
        Height = height;
    }
}
